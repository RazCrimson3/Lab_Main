#include <stdio.h>

int main()
{
    signed int AAA = 0x7d0000f2, BBB = 0xb500004e;
    unsigned int CCC = 0x7d0000f2, DDD = 0xb500004e;
    print( "\nUnary Operations\n\n" );
    print( "%08x\n", -AAA ); /* ______________________________ */
    print( "%08x\n", ~AAA); /* ______________________________ */
    print( "%08x\n", -BBB ); /* ______________________________ */
    print( "%08x\n", ~BBB); /* ______________________________ */
    print( "\nBinary Bitwise Operations (signed)\n\n" );
    print( "%08x\n", AAA & BBB ); /* ______________________________ */
    print( "%08x\n", AAA ^ BBB ); /* ______________________________ */
    print( "%08x\n", AAA | BBB ); /* ______________________________ */
    print( "%08x\n", AAA << 4 ); /* ______________________________ */
    print( "%08x\n", BBB << 4 ); /* ______________________________ */
    print( "%08x\n", AAA >> 12 ); /* ______________________________ */
    print( "%08x\n", BBB >> 12 ); /* ______________________________ */
    print( "\nBinary Bitwise Operations (unsigned)\n\n" );
    print( "%08x\n", CCC & DDD ); /* ______________________________ */
    print( "%08x\n", CCC ^ DDD ); /* ______________________________ */
    print( "%08x\n", CCC | DDD ); /* ______________________________ */
    print( "%08x\n", CCC << 4 ); /* ______________________________ */
    print( "%08x\n", DDD << 4 ); /* ______________________________ */
    print( "%08x\n", CCC >> 12 ); /* ______________________________ */
    print( "%08x\n", DDD >> 12 ); /* ______________________________ */
    print( "\nBinary Arithmetic Operations (signed)\n\n" );
    print( "%08x\n", AAA + BBB ); /* ______________________________ */
    print( "%08x\n", AAA - BBB ); /* ______________________________ */
    print( "%08x\n", BBB - AAA ); /* ______________________________ */
    print( "%08x\n", AAA * 16 ); /* ______________________________ */
    print( "%08x\n", BBB * 16 ); /* ______________________________ */
    print( "%08x\n", AAA / 4096 ); /* ______________________________ */
    print( "%08x\n", BBB / 4096 ); /* ______________________________ */
    print( "%08x\n", AAA % 4096 ); /* ______________________________ */
    print( "%08x\n", BBB % 4096 ); /* ______________________________ */
    print( "\nBinary Arithmetic Operations (unsigned)\n\n" );
    print( "%08x\n", CCC + DDD ); /* ______________________________ */
    print( "%08x\n", CCC - DDD ); /* ______________________________ */
    print( "%08x\n", DDD - CCC ); /* ______________________________ */
    print( "%08x\n", CCC * 16 ); /* ______________________________ */
    print( "%08x\n", DDD * 16 ); /* ______________________________ */
    print( "%08x\n", CCC / 4096 ); /* ______________________________ */
    print( "%08x\n", DDD / 4096 ); /* ______________________________ */
    print( "%08x\n", CCC % 4096 ); /* ______________________________ */
    print( "%08x\n", DDD % 4096 ); /* ______________________________ */
    print( "\nMasking Operations\n\n" );
    print( "%08x\n", CCC & 0xffff ); /* ______________________________ */
    print( "%08x\n", CCC | 0xffff ); /* ______________________________ */
    print( "%08x\n", DDD & 0xffff ); /* ______________________________ */
    print( "%08x\n", DDD | 0xffff ); /* ______________________________ */
    return 0;
}
